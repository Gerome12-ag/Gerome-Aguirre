			<div class="history">
				<h1>Brief History</h1>
				<p>The Eastern Visayas State University had its humble beginnings in 1907, as part of the provincial school. It became a separate entity in 1915, was renamed the Leyte Trade School, and was funded by the provincial government. In 1953, after 38 years, it was renamed the National Provincial Trade School by virtue of R.A. 406 and funded jointly by the National and Provincial Government to cover a wider curriculum. In 1961, the Congress of the Philippines passed Republic Act No. 1561, converting the school into the Leyte Regional School of Arts and Trades, authorizing it to become a training institution for vocational and industrial education in Eastern Visayas.</p>

				<p>On June 19, 1965, Republic Act 4572 was enacted by Congress of the Philippines which converted the school further into a chartered college, renaming it the Leyte Institute of Technology. It took effect beginning in the school year 1965.</p>

				<p>Starting in 1999, the institute has grown to establish a satellite campus in Ormoc City. Pursuant to Board Resolutions No. 59, series of 1999, two CHED supervised institutions (CSIs) in Leyte, namely the Leyte College of Arts and Trades and the Burauen Polytechnic College were integrated to the Leyte Institute of Technology. The LIT Dulag Campus started in SY 2000-2001. The Carigara School of Fisheries was added, the second phase of CSIs institution to SUCs.</p>

				<p>On August 7, 2004, Republic Act No. 9311 was passed converting the Leyte Institute of Technology, into Eastern Visayas State University.</p>

